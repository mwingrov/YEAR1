#include "libft.h"

int	ft_atoi(const char *str)
{
	int	r;
	int	i;
	int	s;

	i = 0;
	s = 1;
	r = 0;
	while ((str[i] == ' ') || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && ft_isdigit(str[i]))
	{
		r = (r * 10) + (str[i] - '0');
		i++;
	}
	return (r * s);
}
